package road;

/**
 * Klasa przechowujace mozliwe kierunki nachylenia drogi
 */
public enum RoadDirections {
    /**
     * Droga przebiega prosto, brak zakretu
     */
    PROSTO,
    /**
     * Zakret przebiega w lewa strone
     */
    LEWO,
    /**
     * Zakret przebiega w prawa strone
     */
    PRAWO
}
